package day3;

public class Loop_Length {
    public static void main(String[] args)
    {
        String name="Loukya";
        System.out.println(name.length());//gives length that count from 1
        System.out.println(name.charAt(2));//prints the char array
        for(int i=0;i<name.length();i++)
            {System.out.println(name.charAt(i));
            }

    }
}
